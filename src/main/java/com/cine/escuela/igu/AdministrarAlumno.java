    public void actualizarTablaAlumnos(){
        
        DefaultTableModel tabla = new DefaultTableModel(){
            
            //filas y columnas no editables
            @Override
            public boolean isCellEditable (int row, int column){
                return false;
            }
        };
        //establecer nombre y columnas
        String titulos[] = {"Legajo", "Apellido", "Nombre", "DNI"};
        tabla.setColumnIdentifiers(titulos);
        
        //carga de los datos desde la base de datos
        List <DTOAlumno> listaAlumnos = controla.traerAlumnos();
        
        
        //recorrer la lista y mostrar cada uno de llos elementos en la tabla
        if (listaAlumnos !=null){
            for (DTOAlumno DTOalu : listaAlumnos){
                Object[] objeto = {DTOalu.getLegajo(),DTOalu.getApellido(),DTOalu.getNombre(),DTOalu.getDni()};
                
                tabla.addRow(objeto);
            }
        }
        jTableAlumnos.setModel(tabla);
    }
    public void mostrarMensajes(String mensaje, String tipo,String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
            
        }
        else if (tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
    
    
